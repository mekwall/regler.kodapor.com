<h1>Kodapornas Regelverk</h1>

<p>
  Kodapor är en Facebook-grupp med tiotusentals medlemmar. För att volymen skall
  bli så hanterbar som möjligt så gallrar moderatorapor bland inläggen och utgår
  från detta regelverk i sin bedömning.
</p>

<blockquote>
  Ämnet som inlägget berör skall
  <span>inbjuda till diskussion</span>, hålla en
  <span>hög intellektuell nivå</span>, vara
  <span>väldigt roligt</span>, och/eller
  <span>direkt intressant</span> för en absolut 
  majoritet av medlemmarna i gruppen.
</blockquote>

<p>
  Exempel på ämnen som
  <span class="always-deleted">alltid blir borttagna</span>:
</p>

<ul>
  <li>Jobbannonser (både som arbetsgivare och arbetstagare)</li>
  <li>Enkäter som enbart gynnar frågeställaren</li>
  <li>
    Snäva tekniska frågor som har ett objektivt rätt svar. Dessa passar mycket
    bättre på <a href="https://www.stackoverflow.com/">StackOverflow</a>
  </li>
  <li>
    Säkerhetsluckor (som man inte först rapporterat till ägaren av problemet)
  </li>
  <li>Diskussioner som vi redan haft flera gånger samma år</li>
  <li>
    Frågor om framförallt fotboll, men även sport i allmänhet som inte
    innefattar kod
  </li>
</ul>

<p>
  Exempel på ämnen som <span class="often-deleted">ofta blir borttagna</span>:
</p>

<ul>
  <li>Skojiga bilder/memes</li>
  <li>Shaming, t.ex. inspektera Nordeas ful-JavaScript</li>
</ul>

<p>
  Exempel på ämnen som <span class="never-deleted">inte blir borttagna</span>:
</p>

<ul>
  <li>Intressanta, öppna diskussioner om kod</li>
  <li>Annonser om hackathons</li>
  <li>Roliga hårdvaruhack</li>
  <li>
    Överdumma diskussioner, t.ex. hur man designar ett så dåligt språk som
    möjligt
  </li>
</ul>

<h2>Q&A</h2>

<h3>Q: Varför har min kommentar blivit borttagen?</h3>
<p>
  A: Kodapor är inget demokratiskt forum, utan är en privat grupp som blivit
  populär på grund av att vi har en speciell kultur här som folk gillar, som
  moderatorerna har som uppgift att värna om. I undantagsfall innebär det att vi
  plockar bort inlägg.
</p>
<p>Våra riktlinjer för att göra detta är dessa:</p>
<h4>Professionalism över vulgaritet</h4>
<p>
  Kodapor är (eller aspirerar till att bli) professionella programmerare, och
  iträder den rollen på forumet så väl som på sin dagliga arbetsplats. Kodapor
  uppskattar idioti och ogillar torrhet, men låter inte detta bli en ursäkt för
  att råka bli vulgär, dömande, intolerant eller exkluderande.
</p>
<h4>Förståelse framför retorik</h4>
<p>
  En kodapa försöker att tolka välvilligt snarare än illvilligt, och söker
  ömsesidigt lärande framför att „vinna“ en diskussion.
</p>
<h4>Humor över allvar</h4>
<p>
  En Kodapa är ödmjuk inför universums absurditet, bjuder på sig själv, och
  tycker inte att en diskussion är fullständig om man inte haft roligt medan man
  fört den. Bajs.
</p>

<h3>Q: Vem söker ni som moderatorapa?</h3>
<p>
  A: Vi behöver nästan alltid fler moderatorapor. Om du har förslag på en
  rättrådig apa som skulle passa, skicka då tipset i ett PM till en
  moderatorapa.
</p>
<p>
  En moderator förväntas lägga åtminstone 45 minuter i veckan. I praktiken är
  din uppgift att godkänna/avslå inlägg enligt ovanstående riktlinjer och din
  egen goda smak, samt hålla ordning på forumet. Oftast innebär detta bara att
  vara rak, trevlig och skön och väldigt sällan att du faktiskt behöver
  använda någon av dina faktiska krafter som moderatorapa.
</p>
<p>
  Moderatorapor får inget betalt, men Kodapor är ett välkänt forum i Sverige.
  Andra moderatorapor agerar gladeligen referens vid jobbansökningar och
  dylikt.
</p>
<p>Vi söker dig som är en bra balans mellan:</p>
<ul>
  <li>Humoristisk och allvarlig, med övervikt på humoristisk</li>
  <li>Pragmatisk och idealistisk, med övervikt på pragmatisk</li>
  <li>Att göra rätt och att vara överens, med övervikt på vara överens</li>
  <li>Att vara trevlig och ärlig, med övervikt på trevlig</li>
  <li>Att styra och att medla, med övervikt på att medla</li>
</ul>
<p>
  Vi kvoterar inte, men det är jävligt meriterande om du inte är vit man i
  30-40 årsåldern som programmerar React.
</p>

